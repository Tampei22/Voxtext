_current_lang: str = "ro"


def set_lang(lang: str) -> None:
    global _current_lang
    _current_lang = lang


def load_lang() -> None:
    global _current_lang
    from storage.settings import load_app_settings
    _current_lang = load_app_settings().lang


def t(key: str, **kwargs) -> str:
    strings = _STRINGS.get(_current_lang, _STRINGS["ro"])
    text = strings.get(key, _STRINGS["ro"].get(key, key))
    if kwargs:
        try:
            return text.format(**kwargs)
        except (KeyError, ValueError):
            return text
    return text
